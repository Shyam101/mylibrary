package com.example.messaging;

public class FirebaseMessaging {

    int x;
    boolean y;

    public FirebaseMessaging(int x, boolean y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public boolean isY() {
        return y;
    }

    public void setY(boolean y) {
        this.y = y;
    }
}
